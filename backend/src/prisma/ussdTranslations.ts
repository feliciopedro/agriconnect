export function t(lang: string, key: string, vars?: Record<string, any>): string {
  const langKey = (lang || 'en') as keyof typeof T;
  const langSet = T[langKey] || T.en;
  let text = langSet[key as keyof typeof langSet];
  if (text === undefined) {
    text = T.en[key as keyof typeof T.en] || key;
  }
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      text = text.replace(new RegExp(`{${k}}`, 'g'), String(v));
    }
  }
  return text;
}
